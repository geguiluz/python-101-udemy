# Here we're looping through a list
fruit_list = ["Apple", "Banana", "Orange"]
for fruit in fruit_list:
    print(fruit)

# We're going to loop through dictionary values
student_count = {
        "course1": 923,
        "course2": 7738,
        "course3": 112,
    }
for k, v in student_count.items():
    print(f"{k}: {v}")


